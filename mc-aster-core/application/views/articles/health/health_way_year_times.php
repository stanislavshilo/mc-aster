                                    <p class="p_article">В нашем организме есть энергетические каналы, по которым все время транслируется энергия, постоянно сообщаются между собой органы. Даже если некоторые органы прооперированы, энергетический канал все равно сохраняется и воздействовать на него нужно обязательно. Необходимо очень внимательно относиться к биологическим часам, особенно когда дело касается патологии.</p>
                                    <p class="p_article">Итак, весной с первоэлементом «Дерево» ассоциируется печень. Весна -процесс раскрытия, развития, цветения, обновления. Поэтому без печени никакого обновления, никакой обмен веществ невозможен.</p>
                                    <p class="p_article">Через печень прокачивается очень большое количество крови. В ней все процессы протекают быстро. Печень - поочередно то собирает, то выводит кровь. Днем, в процессе активной деятельности, кровь поступает в активно работающие органы, а ночью она возвращается в печень. Максимальное время активности печени - 1 -3 часа ночи.</p>
                                    <p class="p_article">Печень регулирует иммунитет, выводит жиры, защищает наш внутренний мир от вредных патогенных воздействий. Она ответственна за эластичность сухожилий, за так называемые растяжки, за качество наших связок, за суставы и соединительную ткань. Болезни печени, когда они возникают, способствуют болезням суставов, болезням связочного аппарата, судорогам мышц, онемению конечностей. Печень ответственна за глаза.</p>
                                    <p class="p_article">Когда слезятся глаза, когда у вас красные склеры, когда устают глаза, снижается зрительная активность, ухудшается сумеречное зрение - это печень дает знать о том, что ей нужна поддержка и отдых. Печень ответственна за состояние бровей и радужных оболочек. Болезни глаз, вызванные отеками, покраснением, вывернутыми веками, постоянный зуд глаз - все это вызвано избытком энергии в печени.</p>
                                    <p class="p_article">Печень напрямую связана с такими органами, как желудок, поджелудочная железа и селезенка. Необходимо восстановить нормальную работу печени, и мы избавимся от многих проблем с этими органами (смотри пентаграмму - круг разрушения). Печень, так как она постоянно работает и несет большую нагрузку по выработке очень многих ферментов для нашего организма, должна получать постоянно полноценное питание.</p>
                                    <p class="p_article">Когда вы что-то едите, помните, что печень переваривает многие вещества, которые являются ядами. Мясо млекопитающих, сервелат, икру, чипсы, кока-колу, пиво печень переваривает несколько суток. Кофе выводит кальций, а значит, снижает рН крови, а в кислой среде печени очень трудно вырабатывать нормальный холестерин и ряд ферментов, которые помогают работе поджелудочной железы и селезенке.</p>
                                    <p class="p_article">Чем мы можем помочь печени? Весной - очистка 1-1,5 месяца: чай «Секрет императора» и «Лучшая защита» весь сезон «Кордицепс-Линчжи». Для помощи гепотацитам клеткам печени, для восстановления иммунитета капсулы «Светлая мечта» в 21 -23 часа. Для нормализации работы печени необходима жидкость, замена жировой ткани, убрать сахара. Полноценное питание для печени - это «Спирулина» и «Кордицепс и листья оливы», «Кальций».</p>
                                    <p class="p_article">Онкологические заболевания развиваются при избытке сахара, дефиците кальция, дефиците гормонов, отсутствии антиоксидантов.</p>
                                    <p class="p_article">Люди, страдающие хроническим заболеванием печени, за один сезон не справятся с болезнью. Для восстановления нормальной функции печени необходимо один-два года, так как раньше полная регенерация печени не происходит. Особенно это касается людей, страдающих алкоголизмом, курильщикам и перенесшим гепатит.</p>
                                    <p class="p_article">Принимая китайские оздоровительные продукты, мы помогаем организму путем отстраивания одного первоэлемента за другим приводить в порядок весь организм. Китайские оздоровительные продукты - это клеточное питание для нашего организма.</p>
