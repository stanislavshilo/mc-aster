                                    <p class="p_article">Осеннее время года характерно уходом от быстротекущего времени года, от Яньского состояния, от состояния порывов, скорости и перехода в позднее лето, характеризующееся сбором (аккумуляцией) и началом сбережения. Затем организм переходит в осень, которая характеризуется в физиологическом плане нашей жизни подытоживанием всего, что мы сделали, и распределением. Ведущим органом этого времени являются легкие. Они предохраняют весь организм от возможных инфекций, от возникновения разных патологий. Они держат, насколько возможно, равновесие в организме.</p>
                                    <p class="p_article">Легкие выполняют функцию распределения влаги по всему организму. Почки выводят, а легкие распределяют. В это время года очень важно научиться много пить. Мы входим в Иньское состояние. Все затормаживается в природе, и мы входим в замедление.</p>
                                    <p class="p_article">И если мы хотим много двигаться, если мы хотим активной жизни, если мы хотим очень много делать, нужно осенью помогать себе. В те депо, которые у нас есть, мы должны положить то, что нужно положить.</p>
                                    <p class="p_article">Легкие отвечают не только за распределение жидкости, но и за общую терморегуляцию организма, то есть за то, как человек реагирует на перемену температуры тела (на жар и на холод).</p>
                                    <p class="p_article">Легкие регулируют кровоток, отвечают за состояние голоса, горла, голосовых связок. У человека с визгливым тембром голоса - недостаток энергии в легких, а у человека с очень низким голосом - переизбыток энергии.</p>
                                    <p class="p_article">Легкие отвечают за переработку кислорода.</p>
                                    <p class="p_article">У легких парный полый орган - толстый кишечник, в котором происходит колоссальная переработка энергии. И невозможно иметь здоровым один парный орган и больным другой. Поэтому легкие нуждаются в чистоте, и чистоту легких обеспечивает регулярная постоянная работа толстого кишечника.</p>
                                    <p class="p_article">В организме взрослого человека содержится от 8 до 25 килограммов каловых завалов. Вот почему так важна чистка организма.</p>
                                    <p class="p_article">Поэтому начинать оздоровление организма нужно с чая «Секрет императора». Даже если вы не страдали запорами, это не значит, что у вас в кишечнике все чисто.</p>
                                    <p class="p_article">В день по данным Всемирной организации здравоохранения (ВОЗ) человек должен получать ежедневно 40-60 граммов клетчатки. Тогда мы обеспечим эвакуацию того количества токсических веществ, которое каждый из нас получает из воздуха, из пищи, путем медикаментозного воздействия.</p>
                                    <p class="p_article">Все лекарства, которые мы принимаем, являются токсическими агентами и подлежат выведению из организма. Иначе они начинают разрушать печень. Печень должна перерабатывать те токсины, которые не выведены из толстого кишечника.</p>
                                    <p class="p_article">Осенью и зимой мы меньше едим свежей клетчатки (фрукты и овощи), нам хочется согреться, и мы едим мясо. Мясом мы насыщаемся быстро, но мясо животных, особенно живущих в нашей полосе, несет в себе колоссальное количество токсинов. И печень несет на себе огромную нагрузку, т. к. она выводит соли мочевой кислоты, а это - тяжелые токсины.</p>
                                    <p class="p_article">Очень хорошо делать комбинацию из чаев «Секрет императора» и «Лучшая защита». Чай принимать теплым, его нужно пить после заварки или использовать как концентрат, добавляя горячую воду.</p>
                                    <p class="p_article">Но вместе с толстым кишечником мы не должны забывать о легких. Для этого у нас есть «Прополис-Линчжи» и чай «Сила природы». Вместе они регулируют иммунитет и являются профилактикой любого инфекционного заболевания. Но принимать их необходимо длительно, желательно на протяжении всего сезона. «Прополис-Линчжи» принимать с 9-11 утра или в 21 -23 часа.</p>
                                    <p class="p_article">Прополис, благодаря своим антибактериальным и антимикробным свойствам, помогает нам преодолевать все респираторные заболевания.</p>
                                    <p class="p_article">Осенью и зимой худеть нежелательно, так как это время года направлено на сохранение. Но применение «Спирулины» помогает нормализовать обменные процессы, компенсировать витаминную и минеральную недостаточность и предупредить накопление веса.</p>
                                    <p class="p_article">Легким свойственно состояние грусти и печали. Позаботясь о легких, мы этим чувствам не позволим зашкаливать, потому что все наши заболевания носят психосоматический характер.</p>
                                    <p class="p_article">Восточные медики говорят, что не существует наследственных заболеваний, а есть наследственные мысли. Мы все думаем так, как нас учили, мы привыкли к определенному сценарию. Поэтому, если легкие перегружены, а это обязательно будет при привычных запорах, ваша печаль может перейти в депрессию.</p>
                                    <p class="p_article">Когда мы позаботимся о двух ведущих органах, распределение влаги, кислорода, питательных веществ пойдет нормальным путем, и у нас все будет хорошо.</p>
                                    <p class="p_article">Мы переходим в очень важное для организма время - переходим в зиму.</p>
