                                    <p class="p_article">Зима - время почек.</p>
                                    <p class="p_article">Почки - это удивительные органы. Хотя они парные, важность каждой из них для человека переоценить невозможно. В организме все взаимосвязано.</p>
                                    <p class="p_article">Почки - орган, отвечающий за то, почему мы с вами вообще живем. Этот орган связан с генетикой. Он отвечает за то, какие мы и что мы передаем своим детям. Правая почка - это мужская почка, несущая все, что связано с передачей генетической информации через сперму. Когда убирают правую почку, или когда есть заболевание правой почки, то в характере все больше исчезают мужские качества и начинают преобладать женские (мелочность, сварливость, придирчивость). То же самое у женщины.</p>
                                    <p class="p_article">Левая половина тела - Иньская (женская). Правая - Яньская (мужская).</p>
                                    <p class="p_article">Почки отвечают за соотношение энергий, за генетику, за усвоение энергии. Они отвечают за кости, за все, что связано с костным аппаратом. Они отвечают за зубы, волосы. Входные ворота почек - это слух. И если падает слух или начинают идти выделения из ушей, это виноваты почки. Падение слуха всегда напрямую связано с функцией почек.</p>
                                    <p class="p_article">Почки - источник энергии в нашем организме. Люди с больными почками - это те, у кого нет сил, у них постоянные боли в пояснице, у них болят ноги. Почки отвечают за общую терморегуляцию: мерзнем мы больше или меньше, знобит ли нас, плохо ли мы переносим жару, как мы приспосабливаемся к перемене температур.</p>
                                    <p class="p_article">Наследственная энергия, которую дают родители нам во время зачатия и которая передается дальше нашим детям, расположена в почках. Поэтому для почек нежелательна инфекция, нежелательна беспорядочная, хаотическая половая жизнь, которые приводят к истощению наследственной энергии. Почки отвечают за выработку костного мозга, который, в свою очередь, способствует кровообращению. Злокачественные заболевания кровеносной системы напрямую связаны с тем, что делается в почках.</p>
                                    <p class="p_article">Почки выводят шлаки и мочу, и если они работают плохо, то шлаки остаются в нашем организме. Почки требуют влагу и питательные вещества.</p>
                                    <p class="p_article">Китайцы рассматривают почки, и надпочечники, и урогенитальную систему (то есть гинекологию и урологию) как единую систему. Почки запрограммированы на биологическую жизнь от 200 до 800 лет.</p>
                                    <p class="p_article">Для поддержания почек зимой, даже если нет никаких проблем, необходимо пропить цикл «Кордицепса-Линчжи». Зимой необходим кальций. Людям, у которых падает слух, необходим кальциевый фон на протяжении 2-х с половиной месяцев с 17 до 19 часов (одна-две капсулы).</p>
                                    <p class="p_article">Многие женщины сейчас страдают мастопатией. Она опасна тем, что в последнее время все больше и больше переходит в онкозаболевания. Профилактика и лечение мастопатии и всего, что связано с нарушением здоровья женской половой сферы, - это прием «Черной жемчужины» и «Кордицепса».</p>
                                    <p class="p_article">Во время работы меридиана почек необходима «Спирулина». Она содержит цинк, который необходим эпителиальным клеткам слизистой почек и половых желез.</p>
                                    <p class="p_article">Во время элемента «Вода» должны особое внимание уделять периферической кровеносной системе. Большие кровеносные сосуды находятся у первоэлемента «Огонь» (сердце), а мелкие капиллярные сосуды - за них отвечают почки. Вот почему полезен чай «Золотая река».</p>
